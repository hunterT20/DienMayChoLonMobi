package com.dienmaycholon.dienmaycholonmobi.data.model.api;

public class ParamCartProduct {
    private String id;
    private int color;
    private int qt;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getQt() {
        return qt;
    }

    public void setQt(int qt) {
        this.qt = qt;
    }
}
